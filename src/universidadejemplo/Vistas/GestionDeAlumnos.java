    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        int dni = Integer.parseInt(jTDni.getText());
        String apellido = jTApellido.getText();
        String nombre = jTNombre.getText();
        java.util.Date fecha = jDateCalendario.getDate();
        LocalDate fechaN = fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        boolean estado = jCheckEstado.isSelected();
        Alumno alumno = new Alumno(dni,apellido,nombre,fechaN,estado);
        alumData.guardarAlumno(alumno);    
    }//GEN-LAST:event_jBGuardarActionPerformed

